/**
 * Add two numbers
 * @param {number} x - First Number
 * @param { number} y - Second Number
 * @returns {number} - Sum of x and y
 */
const add = (x, y) => x + y;

/**
 * Subtract numbers
 * @param {number} x - First Number
 * @param { number} y - Second Number
 * @returns {number} - Subtract of x and y
 */
const subtract = (x, y) => x - y;
